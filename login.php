<?php
require_once 'vendor/autoload.php';

use App\Configs\Session;
use App\Configs\Database;

$session = Session::getInstance();
$error = '';

// If already logged in, redirect to dashboard
if ($session->isLoggedIn()) {
    header('Location: /dashboard.php');
    exit;
}

// Handle login form submission
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = $_POST['username'] ?? '';
    $password = $_POST['password'] ?? '';

    if (empty($username) || empty($password)) {
        $error = 'Please enter both username and password';
    } else {
        // In a real application, you would verify against a database
        // For this internal tool, we'll use a simple hardcoded check
        if ($username === 'admin' && $password === 'your_secure_password_here') {
            $session->set('user_role', 'admin');
            $session->set('user_id', 1);
            $session->setFlashMessage('success', 'Welcome back!');
            header('Location: /dashboard.php');
            exit;
        } else {
            $error = 'Invalid username or password';
        }
    }
}
